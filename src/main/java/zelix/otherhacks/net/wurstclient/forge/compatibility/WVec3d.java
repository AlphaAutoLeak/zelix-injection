package zelix.otherhacks.net.wurstclient.forge.compatibility;

import net.minecraft.util.math.*;

public final class WVec3d
{
    public static double getX(final Vec3d vec) {
        return vec.x;
    }
    
    public static double getY(final Vec3d vec) {
        return vec.y;
    }
    
    public static double getZ(final Vec3d vec) {
        return vec.z;
    }
}
